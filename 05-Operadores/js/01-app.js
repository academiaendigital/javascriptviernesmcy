// Sobre operadores, previamente ya vimos que existen operadores de
//  suma, resta, multiplicación, pero existen unos
//  muy útiles de compraración

const numero1 = 20;
const numero2 = "20";
const numero3 = 30;

// Operador Mayor a 
console.log( numero1 > numero3 );
console.log( numero3 > numero1 );


// Operador Menor que
console.log(numero3 < numero1);



const numero4 = 20;
const numero5 = "20";
const numero6 = 30;


// Revisar si 2 valores son iguales o diferentes
console.log(numero4 == numero5);
console.log(numero4 == numero6);


// Typeof
console.log( typeof numero1 );
console.log( typeof numero2 );

// Operador estricto (revisa valor y tipo de dato)
console.log(numero4 === numero5 );

// Diferente a 

console.log(numero1 != numero3);
console.log(numero1 != numero2);
console.log(numero1 !== numero2);

